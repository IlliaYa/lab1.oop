using System;

class ValToVar
{
    static void Main(string[] args)
    {
        int value1 = -100, value2 = 128, value3 = -3540, value4 = 64876;
        long value5 = 2147483648, value6 = -1141583228, value7 = -1223372036854775808;
        Console.WriteLine("output: {0} \t {1}\t {2}\t {3}\t {4}\t {5}\t {6}", value1, value2, value3, value4, value5, value6, value7);
    }
}
